package atulProject;

public class SizeOfDataTypes {

	public static void main(String[] args) {
			System.out.println("Minimum value of an integer " +Integer.MIN_VALUE);
			System.out.println("Maximum value of an integer " +Integer.MAX_VALUE);
			System.out.println("Bytes "+Integer.BYTES);
			System.out.println();
			
			System.out.println("Minimum value of an Byte " +Byte.MIN_VALUE);
			System.out.println("Maximum value of an Byte " +Byte.MAX_VALUE);
			System.out.println("Bytes "+Byte.BYTES);
			System.out.println();
			
			System.out.println("Minimum value of an Long " +Long.MIN_VALUE);
			System.out.println("Maximum value of an Long " +Long.MAX_VALUE);
			System.out.println("Bytes "+Long.BYTES);
			System.out.println();
			
			System.out.println("Minimum value of an double " +Double.MIN_VALUE);
			System.out.println("Maximum value of an double " +Double.MAX_VALUE);
			System.out.println("Bytes "+Character.BYTES);
			
			System.out.println("Minimum value of an Sort " +Short.MIN_VALUE);
			System.out.println("Maximum value of an Short " +Short.MAX_VALUE);
			System.out.println("Bytes "+Short.BYTES);
			
	}

}
